<?php

$numeradorA = (6 * (3 + 2 )) ** 2;
$denominadorA = 3 * 2;

$numeradorB = (1 - 5) * (2 - 7);
$denominadorB = 2;

$superiorA = $numeradorA / $denominadorA;
$superiorB = ($numeradorB / $denominadorB) ** 2;

$superiorTotal = ($superiorA - $superiorB) ** 3;
$denominadorC = 10 ** 3;

$resultado = $superiorTotal / $denominadorC;
echo "O resultado é: " . $resultado . ".";

